import pandas as pd
import matplotlib.pyplot as plt

def tcs():
    df = pd.read_csv("tcs.csv")
    plt.style.use("bmh")
    x = df['Date']
    y = df["Close"]
    plt.xlabel("Date")
    plt.ylabel("Close")
    plt.plot(x,y)
    plt.title("tcs stock")
    plt.show()
tcs()
